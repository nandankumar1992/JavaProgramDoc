package practise;



class Swapping{  
    public static void main(String[] args) {  
       int x = 1, y = 2, t;// x and y are to swap   
       System.out.println("before swapping numbers: "+x +"  "+ y);  
       /*swapping */  
       t = x;  
       x = y;  
       y = t;  
       System.out.println("After swapping: "+x +"   " + y);  
       System.out.println( );  
    }    
}  